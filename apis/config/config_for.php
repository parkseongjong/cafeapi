function logWrite($arr='') {
    $fname = "/var/www/html/apis/for/logs/" . date('Y-m-d') . ".txt";
    $f = fopen($fname, "a");
    fwrite($f, "[".date('Y-m-d H:i:s')."] : ".$_SERVER['REMOTE_ADDR']."\n");
    fwrite($f, "[REQ] ---------------\n");
    foreach($_GET as $k => $v) {
        fwrite($f, '    '.$k.'='.$v."\n");
    }
    fwrite($f, "[RET] ---------------\n");
    if (is_array($arr)) {
        foreach($arr as $k => $v) {
            if (is_array($v)) {
                fwrite($f, '    '.$k.'='.print_r($v, true)."\n");
            } else {
                fwrite($f, '    '.$k.'='.$v."\n");
            }
        }
    } else {
        fwrite($f, '    '.$arr."\n");
    }
    fwrite($f, "\n");
    fwrite($f, "========================================\n\n");
    fclose($f);
}


function jsonReturn($arr='') {
    if (empty($arr)) {
        $arr = array('code'=>'999', 'error'=>true, 'msg'=>'Error');
        echo json_encode($arr, JSON_UNESCAPED_UNICODE);
    } else {
        if (is_array($arr)) {
            echo json_encode($arr, JSON_UNESCAPED_UNICODE);
        } else {
            echo json_encode(array('code'=>'999', 'error'=>true, 'msg'=>$arr), JSON_UNESCAPED_UNICODE);
        }
    }
    //logWrite($arr);
    exit();
}


function err_message ($err_code) {
	$msg = '';
	switch($err_code) {
		case 200:
			$msg = 'Success';
			break;

		case 801:
			// 허용되지 않은 사용자입니다. (code값 불일치)
			$msg = 'Disallowed user';
			break;
		case 802:
			// 잘못된 요청입니다. (kind값 불일치)
			$msg = 'Bad request';
			break;

		case 804:
			// 필수값이 누락되었습니다.
			$msg = 'Missing required value';
			break;
		case 805:
			// 허용되지 않은 값이 입력되었습니다.
			$msg = 'Unacceptable value entered';
			break;
		case 806:
			// 정보가 존재하지 않습니다.
			$msg = 'No information';
			break;
		case 807:
			// 송장 유효기간이 만료되었습니다.
			$msg = 'Expired and unavailable';
			break;
		case 808:
			// 조회 실패
			$msg = 'Lookup failure';
			break;
		case 809:
			// 처리 실패
			$msg = 'Processing failure'; // usdt 전송시 실패한 경우
			break;
		case 811:
			// 저장 중 오류가 발생했습니다.
			$msg = 'Error during saving';
			break;
		case 999:
			$msg = 'Error';
			break;
	}
	return $msg;
}


function create_invoice_number() {
	$r = date('d').strrev(time()).rand(1000,9999);
	$count = get_for_transaction_list('order_num', $r, 'count');
	if ( $count > 0 ) {
		$r = create_invoice_number();
	}
	return $r;
}

function get_for_transaction_list($type, $val, $res_type) {
	if ( $type == 'order_num' ) {
		$db = getDbInstance();
		$db->where('order_num', $val);
		if ( $res_type == 'count' ) {
			$result = $db->getValue('for_transaction_list', 'count(*)');
		} else if ( $res_type == 'list' ) {
			$result = $db->getOne('for_transaction_list');
		}
	}
	return $result;
} //

